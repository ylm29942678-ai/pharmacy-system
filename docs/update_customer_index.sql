-- ========================================================
-- 顾客表索引更新脚本
-- 用途：移除手机号唯一索引，改为普通索引，支持姓名和手机号重复
-- ========================================================

-- 如果数据库中存在唯一索引，则删除
DROP INDEX IF EXISTS `uk_phone` ON `customer`;

-- 添加普通索引用于查询
CREATE INDEX IF NOT EXISTS `idx_phone` ON `customer`(`phone`);

-- ========================================================
-- 验证索引是否正确
-- ========================================================
SHOW INDEX FROM `customer`;

